// buildNamePool — używa FIRST/LAST lub FIRST_EN/LAST_EN zależnie od LANG
// (nadpisuje oryginalne buildNamePool poniżej — WAŻNE: ta definicja musi być PRZED oryginalną)
// Oryginalna funkcja buildNamePool() zostaje w kodzie, ale wywołujemy nową przez buildNamePoolI18n()
function buildNamePoolI18n() {
  const firstArr = LANG === 'en' ? FIRST_EN : FIRST;
  const lastArr  = LANG === 'en' ? LAST_EN  : LAST;
  const pool = [];
  const used = new Set();
  const allCombos = [];
  for (const f of firstArr) {
    for (const l of lastArr) {
      allCombos.push(f + ' ' + l);
    }
  }
  for (let i = allCombos.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [allCombos[i], allCombos[j]] = [allCombos[j], allCombos[i]];
  }
  for (const name of allCombos) {
    if (!used.has(name)) { used.add(name); pool.push(name); }
    if (pool.length >= 2000) break;
  }
  return pool;
}

