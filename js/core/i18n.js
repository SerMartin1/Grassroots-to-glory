// Funkcja tłumaczenia — fallback na PL jeśli brak klucza EN
// Użycie: t('klucz') lub t('klucz').replace('{x}', wartość)
function t(key) {
  return T[LANG][key] || T['pl'][key] || key;
}

// Ustaw język i zapisz — wywoływana z lang pickera
function setLang(l) {
  LANG = l;
  localStorage.setItem('gtg_lang', l);
  _syncLangButtons();
  applyLang();
}

